// server/services/pdf/generic_incident.js — WI-09 universal incident
// report PDF.
//
// Per PRD §4.6, this is the platform's fallback record artifact for
// any incident regardless of jurisdiction, classification, or
// completeness. It is NOT a regulatory submission — the footer makes
// that explicit. Where regulatory reporting is required, submit
// through the appropriate regulator's channels (OSHA ITA, HSE RIDDOR,
// SafeWork NSW Notify, etc.).
//
// Architectural notes:
//   • Reuses the pdfkit pattern from services/pdf/osha_300.js + 300a +
//     301: margins.bottom: 0, lineBreak: false on every text() call,
//     manual word-wrap via wrapLines() for free-text fields. See
//     memory feedback_pdfkit_autopagination for the rationale.
//   • Multi-page by design. Pagination is MANUAL — each draw helper
//     receives a cursor `y` and returns the new `y`, AND requests a
//     new page via the `nextPageIfNeeded()` helper when the next block
//     wouldn't fit.
//   • Section visibility is opt-in via the `sections` array; the route
//     defaults to all sections enabled.
//   • Customer-brandable: org name in the header, footer carries
//     "Generated by [platform] on [date]" + the internal-record
//     disclaimer. NO logo embedding — organizations table has no logo
//     column today, and the spec forbids external URL fetches.

import PDFDocument from 'pdfkit';
import { embedOrgLogo } from './logo.js';

const PAGE_OPTS = {
  layout: 'portrait',
  size: 'LETTER',
  margins: { top: 36, bottom: 0, left: 36, right: 36 },
};

