/**
 * Stream the generic incident PDF to `res`. The route layer loads all
 * the payload pieces (incident, site, AP+injuries, investigation,
 * fiveWhys, capas, classifications, attachments, auditEntries, orgName)
 * and passes them here.
 *
 *   payload = {
 *     orgName, site,
 *     incident,                  // joined row
 *     affectedPersons: [...],    // each with injuries: [...]
 *     investigation, investigationLead, fiveWhys,
 *     capas,                     // with owner_name + verifier_name resolved
 *     oshaSevereRows, riddorReport, nswNotification,
 *     attachments,
 *     auditEntries,
 *     sections: ['overview', ...],  // which to render; defaults to ALL_SECTIONS
 *     generatedAt,               // ISO string for the footer
 *   }
 */
export function renderGenericIncidentPdf(res, payload) {
  // Section filter — default ALL_SECTIONS. If the caller passed a
  // non-empty list whose every entry is unknown, fall back to defaults
  // rather than rendering an empty document. Mirrors the same fallback
  // the route applies, but enforces it here too so direct callers
  // (tests, future integrations) get the same behaviour.
  let sections = ALL_SECTIONS;
  if (Array.isArray(payload.sections) && payload.sections.length > 0) {
    const filtered = payload.sections.filter(s => SECTION_FN[s]);
    if (filtered.length > 0) sections = filtered;
  }

  const doc = new PDFDocument(PAGE_OPTS);
  doc.pipe(res);

  const ctx = {
    orgName: payload.orgName,
    orgLogoPath: payload.orgLogoPath || null,
    incident: payload.incident,
    site: payload.site,
    affectedPersons: payload.affectedPersons,
    investigation: payload.investigation,
    investigationLead: payload.investigationLead,
    fiveWhys: payload.fiveWhys,
    capas: payload.capas,
    oshaSevereRows: payload.oshaSevereRows,
    riddorReport: payload.riddorReport,
    nswNotification: payload.nswNotification,
    attachments: payload.attachments,
    auditEntries: payload.auditEntries,
    pageIndex: 1,
  };

  drawFirstPageHeader(doc, ctx);
  let y = 100;

  for (const sec of sections) {
    const fn = SECTION_FN[sec];
    if (!fn) continue;
    y = fn(doc, y, ctx);
  }

  // Footer on the LAST page only. The internal-record disclaimer is
  // the same on every page; running it once at the end keeps the body
  // surface uncluttered while still surfacing the legal notice.
  drawFooter(doc, payload.generatedAt || new Date().toISOString().slice(0, 10));

  doc.end();
}
