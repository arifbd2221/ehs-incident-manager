function sectionClassifications(doc, y, ctx) {
  const { incident, oshaSevereRows, riddorReport, nswNotification } = ctx;
  y = drawSectionHeader(doc, LEFT_X, y, USABLE_W, 'Regulatory classifications', ctx);

  let any = false;

  // OSHA recordability
  if (incident.osha_recordable !== null && incident.osha_recordable !== undefined) {
    any = true;
    y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'OSHA recordable',
      incident.osha_recordable ? `Yes (${incident.osha_recordability_type || ''})` : 'No', ctx);
    if (incident.osha_recordable) {
      y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Days away',    incident.osha_days_away, ctx);
      y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Days restricted', incident.osha_days_restricted, ctx);
      if (incident.osha_date_of_death) {
        y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Date of death', fmtDate(incident.osha_date_of_death), ctx);
      }
    }
  }

  // OSHA 1904.39 severe-injury notifications
  if (Array.isArray(oshaSevereRows) && oshaSevereRows.length > 0) {
    any = true;
    y = drawSubsectionLabel(doc, LEFT_X, y + 4, '1904.39 severe-injury notifications', ctx);
    for (const r of oshaSevereRows) {
      y = drawFieldRow(doc, LEFT_X + 8, y, USABLE_W - 8,
        r.category, `Deadline ${fmtDateTime(r.deadline_at)} · ${r.phone_notified_at ? 'Phone notified ' + fmtDateTime(r.phone_notified_at) : 'Phone pending'}`, ctx);
    }
  }

  // RIDDOR
  if (incident.riddor_reportable || riddorReport) {
    any = true;
    y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'RIDDOR reportable',
      incident.riddor_reportable ? `Yes${incident.riddor_category ? ` (${incident.riddor_category})` : ''}` : 'No', ctx);
    if (riddorReport) {
      y = drawFieldRow(doc, LEFT_X + 8, y, USABLE_W - 8, 'Reference', riddorReport.riddor_number, ctx);
      y = drawFieldRow(doc, LEFT_X + 8, y, USABLE_W - 8, 'Phone notified', fmtDateTime(riddorReport.phone_notified_at), ctx);
      y = drawFieldRow(doc, LEFT_X + 8, y, USABLE_W - 8, 'Written submitted', fmtDateTime(riddorReport.written_submitted_at), ctx);
      y = drawFieldRow(doc, LEFT_X + 8, y, USABLE_W - 8, 'Written deadline', fmtDateTime(riddorReport.written_deadline), ctx);
    }
  }

  // SafeWork NSW
  if (nswNotification) {
    any = true;
    y = drawSubsectionLabel(doc, LEFT_X, y + 4, 'SafeWork NSW notification', ctx);
    y = drawFieldRow(doc, LEFT_X + 8, y, USABLE_W - 8, 'NSW number',   nswNotification.nsw_number, ctx);
    y = drawFieldRow(doc, LEFT_X + 8, y, USABLE_W - 8, 'Fatality',     nswNotification.is_fatality ? 'Yes' : 'No', ctx);
    y = drawFieldRow(doc, LEFT_X + 8, y, USABLE_W - 8, 'Serious injury', nswNotification.is_serious_injury ? 'Yes' : 'No', ctx);
    y = drawFieldRow(doc, LEFT_X + 8, y, USABLE_W - 8, 'Dangerous incident', nswNotification.is_dangerous_incident ? 'Yes' : 'No', ctx);
    y = drawFieldRow(doc, LEFT_X + 8, y, USABLE_W - 8, 'Phone notified', fmtDateTime(nswNotification.phone_notified_at), ctx);
    y = drawFieldRow(doc, LEFT_X + 8, y, USABLE_W - 8, 'Written submitted', fmtDateTime(nswNotification.written_submitted_at), ctx);
    y = drawFieldRow(doc, LEFT_X + 8, y, USABLE_W - 8, 'Site preservation', nswNotification.site_preservation_status, ctx);
  }

  if (!any) {
    y = drawWrapped(doc, LEFT_X, y, USABLE_W, 'No regulatory classifications recorded.', 9, '#666A72', FONT_ITAL);
  }
  return y + 8;
}

function sectionAttachments(doc, y, ctx) {
  const { attachments } = ctx;
  y = drawSectionHeader(doc, LEFT_X, y, USABLE_W, 'Attachments', ctx);
  if (!Array.isArray(attachments) || attachments.length === 0) {
    y = drawWrapped(doc, LEFT_X, y, USABLE_W, 'No attachments uploaded.', 9, '#666A72', FONT_ITAL);
    return y + 8;
  }
  for (const a of attachments) {
    y = nextPageIfNeeded(doc, y, 16, ctx);
    doc.font(FONT_BOLD).fontSize(8).fillColor('#1A1A1A');
    doc.text(`• ${a.filename}`, LEFT_X, y, { width: USABLE_W * 0.55, lineBreak: false, ellipsis: true });
    doc.font(FONT_REG).fontSize(8).fillColor('#666A72');
    const meta = [
      a.mime_type,
      a.size_bytes ? `${Math.round(a.size_bytes / 1024)} KB` : null,
      a.created_at ? fmtDate(a.created_at) : null,
    ].filter(Boolean).join(' · ');
    doc.text(meta, LEFT_X + USABLE_W * 0.6, y, { width: USABLE_W * 0.4, align: 'right', lineBreak: false, ellipsis: true });
    if (a.description) {
      y += 11;
      y = drawWrapped(doc, LEFT_X + 12, y, USABLE_W - 12, a.description, 8, '#52525F', FONT_REG);
    } else {
      y += 12;
    }
    y += 2;
  }
  return y + 6;
}

function sectionAudit(doc, y, ctx) {
  const { auditEntries } = ctx;
  y = drawSectionHeader(doc, LEFT_X, y, USABLE_W, 'Audit trail (most recent entries)', ctx);
  if (!Array.isArray(auditEntries) || auditEntries.length === 0) {
    y = drawWrapped(doc, LEFT_X, y, USABLE_W, 'No audit entries.', 9, '#666A72', FONT_ITAL);
    return y + 8;
  }
  for (const e of auditEntries) {
    y = nextPageIfNeeded(doc, y, 18, ctx);
    doc.font(FONT_REG).fontSize(7).fillColor('#666A72');
    doc.text(fmtDateTime(e.created_at), LEFT_X, y, { width: 110, lineBreak: false });
    doc.font(FONT_BOLD).fontSize(7).fillColor('#1A1A1A');
    doc.text(e.action || '', LEFT_X + 110, y, { width: 130, lineBreak: false, ellipsis: true });
    doc.font(FONT_REG).fontSize(7).fillColor('#1A1A1A');
    const desc = `${e.user_name ? '[' + e.user_name + '] ' : ''}${e.description || ''}`;
    const lines = wrapLines(doc, desc, USABLE_W - 240, FONT_REG, 7);
    for (let i = 0; i < lines.length; i++) {
      doc.text(lines[i], LEFT_X + 240, y + i * 9, { width: USABLE_W - 240, lineBreak: false });
    }
    y += Math.max(10, lines.length * 9) + 2;
  }
  return y + 6;
}

const SECTION_FN = {
  overview:         sectionOverview,
  affected_persons: sectionAffectedPersons,
  investigation:    sectionInvestigation,
  causes:           sectionCauses,
  capas:            sectionCapas,
  classifications:  sectionClassifications,
  attachments:      sectionAttachments,
  audit:            sectionAudit,
};

