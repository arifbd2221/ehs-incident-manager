function sectionOverview(doc, y, ctx) {
  const { incident, site } = ctx;
  y = drawSectionHeader(doc, LEFT_X, y, USABLE_W, 'Incident overview', ctx);
  y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Number',         incident.incident_number, ctx);
  y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Title',          incident.title, ctx);
  y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Type',           incident.type, ctx);
  y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Severity',       incident.severity != null ? `S${incident.severity}` : null, ctx);
  y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Track',          incident.track, ctx);
  y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Status',         incident.status, ctx);
  y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Incident date',  fmtDateTime(incident.incident_datetime), ctx);
  y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Site',           site?.name, ctx);
  y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Area / location', [incident.area, incident.specific_location].filter(Boolean).join(' · ') || null, ctx);
  y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Department',     incident.department, ctx);
  if (incident.description) {
    y = drawSubsectionLabel(doc, LEFT_X, y + 4, 'Description', ctx);
    y = nextPageIfNeeded(doc, y, 36, ctx);
    y = drawWrapped(doc, LEFT_X, y, USABLE_W, incident.description, 9, '#1A1A1A', FONT_REG);
  }
  if (incident.immediate_actions_taken) {
    y = drawSubsectionLabel(doc, LEFT_X, y + 6, 'Immediate actions taken', ctx);
    y = drawWrapped(doc, LEFT_X, y, USABLE_W, incident.immediate_actions_taken, 9, '#1A1A1A', FONT_REG);
  }
  return y + 12;
}

function sectionAffectedPersons(doc, y, ctx) {
  const { affectedPersons } = ctx;
  if (!Array.isArray(affectedPersons) || affectedPersons.length === 0) {
    // Render the header + "Not recorded" rather than hide entirely —
    // an inspector wants to see the section was checked, just empty.
    y = drawSectionHeader(doc, LEFT_X, y, USABLE_W, 'Affected persons', ctx);
    y = drawWrapped(doc, LEFT_X, y, USABLE_W, 'No affected persons recorded for this incident.', 9, '#666A72', FONT_ITAL);
    return y + 8;
  }
  y = drawSectionHeader(doc, LEFT_X, y, USABLE_W, 'Affected persons', ctx);
  for (let i = 0; i < affectedPersons.length; i++) {
    const ap = affectedPersons[i];
    y = nextPageIfNeeded(doc, y, 60, ctx);
    doc.save();
    doc.font(FONT_BOLD).fontSize(10).fillColor('#1A1A1A');
    doc.text(
      `${i + 1}. ${ap.name || 'Unnamed'}${ap.is_primary ? ' · primary' : ''}`,
      LEFT_X, y, { width: USABLE_W, lineBreak: false, ellipsis: true },
    );
    doc.restore();
    y += 14;
    y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Job title',          ap.job_title, ctx);
    y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Employment status',  ap.employment_status, ctx);
    y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Date of birth',      fmtDate(ap.dob), ctx);
    y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Date hired',         fmtDate(ap.date_hired), ctx);
    y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Privacy case',       ap.is_privacy_case ? 'Yes' : 'No', ctx);
    if (Array.isArray(ap.injuries) && ap.injuries.length > 0) {
      y = drawSubsectionLabel(doc, LEFT_X, y + 4, 'Injuries', ctx);
      for (const inj of ap.injuries) {
        y = drawFieldRow(doc, LEFT_X + 16, y, USABLE_W - 16, 'Body part',     inj.body_part, ctx);
        y = drawFieldRow(doc, LEFT_X + 16, y, USABLE_W - 16, 'Injury type',   inj.injury_type, ctx);
        y = drawFieldRow(doc, LEFT_X + 16, y, USABLE_W - 16, 'Mechanism',     inj.mechanism, ctx);
        y = drawFieldRow(doc, LEFT_X + 16, y, USABLE_W - 16, 'Treatment',     inj.treatment, ctx);
        y = drawFieldRow(doc, LEFT_X + 16, y, USABLE_W - 16, 'ER treated',    inj.er_treated ? 'Yes' : 'No', ctx);
        y = drawFieldRow(doc, LEFT_X + 16, y, USABLE_W - 16, 'Hospitalized',  inj.hospitalized ? 'Yes' : 'No', ctx);
        if (inj.date_of_death) {
          y = drawFieldRow(doc, LEFT_X + 16, y, USABLE_W - 16, 'Date of death', fmtDate(inj.date_of_death), ctx);
        }
        y = drawSeparator(doc, LEFT_X + 16, y, USABLE_W - 16);
      }
    }
    y = drawSeparator(doc, LEFT_X, y, USABLE_W);
  }
  return y + 6;
}

