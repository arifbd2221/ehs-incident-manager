function fmt(value) {
  if (value === null || value === undefined || value === '') return 'Not recorded';
  return String(value);
}

function fmtDateTime(iso) {
  if (!iso) return 'Not recorded';
  const d = new Date(iso);
  if (Number.isNaN(d.getTime())) return String(iso);
  return d.toISOString().slice(0, 16).replace('T', ' ');
}

function fmtDate(iso) {
  if (!iso) return 'Not recorded';
  const d = new Date(iso);
  if (Number.isNaN(d.getTime())) return String(iso);
  return d.toISOString().slice(0, 10);
}

// Word-wrap helper — emits each line with lineBreak: false so pdfkit
// never auto-paginates mid-paragraph. Returns the lines as an array;
// caller is responsible for drawing them at known y coordinates.
function wrapLines(doc, text, width, font, fontSize) {
  if (!text) return [];
  doc.font(font).fontSize(fontSize);
  const paragraphs = String(text).split(/\r?\n/);
  const lines = [];
  for (const para of paragraphs) {
    const words = para.split(/\s+/).filter(Boolean);
    if (words.length === 0) { lines.push(''); continue; }
    let cur = '';
    for (const word of words) {
      const tentative = cur ? `${cur} ${word}` : word;
      if (doc.widthOfString(tentative) <= width) {
        cur = tentative;
      } else {
        if (cur) lines.push(cur);
        cur = word;
      }
    }
    if (cur) lines.push(cur);
  }
  return lines;
}

function drawWrapped(doc, x, y, width, text, fontSize = 9, color = '#000000', font = FONT_REG) {
  const lineH = fontSize + 3;
  const lines = wrapLines(doc, text, width, font, fontSize);
  doc.font(font).fontSize(fontSize).fillColor(color);
  for (let i = 0; i < lines.length; i++) {
    doc.text(lines[i], x, y + i * lineH, { width, lineBreak: false });
  }
  return y + lines.length * lineH;
}

// Page-overflow guard: if drawing `neededHeight` from the current `y`
// would land past the safe zone (page.height - FOOTER_RESERVE), add a
// new page and reset y to the post-header band. Returns the new y.
function nextPageIfNeeded(doc, y, neededHeight, ctx) {
  if (y + neededHeight <= PAGE_H - FOOTER_RESERVE) return y;
  doc.addPage(PAGE_OPTS);
  ctx.pageIndex += 1;
  drawCompactHeader(doc, ctx);
  return 70;
}

function drawSectionHeader(doc, x, y, w, text, ctx) {
  // Reserve space for the section header itself + a small content
  // breath so we don't orphan a section title at page bottom.
  y = nextPageIfNeeded(doc, y, 32, ctx);
  doc.save();
  doc.lineWidth(0.6).strokeColor('#000000');
  doc.rect(x, y, w, 16).fillAndStroke('#E8E8EE', '#000000');
  doc.fillColor('#000000').font(FONT_BOLD).fontSize(10);
  doc.text(text, x + 4, y + 4, { width: w - 8, lineBreak: false });
  doc.restore();
  return y + 18;
}

function drawFieldRow(doc, x, y, w, label, value, ctx) {
  y = nextPageIfNeeded(doc, y, 22, ctx);
  doc.save();
  doc.font(FONT_BOLD).fontSize(8).fillColor('#52525F');
  doc.text(label, x, y, { width: w * 0.32, lineBreak: false, ellipsis: true });
  doc.font(FONT_REG).fontSize(9).fillColor('#1A1A1A');
  // Allow the value to wrap onto multiple lines if needed.
  const lines = wrapLines(doc, fmt(value), w * 0.65, FONT_REG, 9);
  for (let i = 0; i < lines.length; i++) {
    doc.text(lines[i], x + w * 0.35, y + i * 12, { width: w * 0.65, lineBreak: false });
  }
  doc.restore();
  return y + Math.max(12, lines.length * 12) + 4;
}

function drawSubsectionLabel(doc, x, y, text, ctx) {
  y = nextPageIfNeeded(doc, y, 18, ctx);
  doc.font(FONT_BOLD).fontSize(8).fillColor('#52525F');
  doc.text(text, x, y, { width: USABLE_W, lineBreak: false });
  return y + 12;
}

function drawSeparator(doc, x, y, w) {
  doc.save();
  doc.lineWidth(0.3).strokeColor('#E0E0E0');
  doc.moveTo(x, y).lineTo(x + w, y).stroke();
  doc.restore();
  return y + 6;
}

