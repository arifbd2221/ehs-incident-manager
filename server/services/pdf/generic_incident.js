function drawFirstPageHeader(doc, ctx) {
  const { orgName, incident, orgLogoPath } = ctx;
  doc.save();

  // Logo slot: top-right, 80px wide × 40px tall. No-op when unset.
  const LOGO_W = 90;
  const LOGO_H = 40;
  embedOrgLogo(doc, orgLogoPath, PAGE_W - LEFT_X - LOGO_W, 32, LOGO_W, LOGO_H);

  doc.font(FONT_BOLD).fontSize(16).fillColor('#1A1A1A');
  doc.text(orgName || PLATFORM_NAME, LEFT_X, 36, { width: USABLE_W - LOGO_W - 20, lineBreak: false, ellipsis: true });

  doc.font(FONT_BOLD).fontSize(11).fillColor('#52525F');
  doc.text('Incident Report', LEFT_X, 58, { width: USABLE_W - LOGO_W - 20, lineBreak: false });

  doc.font(FONT_REG).fontSize(9).fillColor('#52525F');
  const subRight = `${incident.incident_number}${incident.title ? ' — ' + incident.title : ''}`;
  doc.text(subRight, LEFT_X, 74, { width: USABLE_W - LOGO_W - 20, lineBreak: false, ellipsis: true });
  doc.restore();
}

function drawCompactHeader(doc, ctx) {
  const { orgName, incident, pageIndex } = ctx;
  doc.save();
  doc.font(FONT_BOLD).fontSize(9).fillColor('#52525F');
  doc.text(
    `${orgName || PLATFORM_NAME} — ${incident.incident_number}`,
    LEFT_X, 28, { width: USABLE_W * 0.75, lineBreak: false, ellipsis: true },
  );
  doc.font(FONT_REG).fontSize(8).fillColor('#666A72');
  doc.text(`Page ${pageIndex}`, LEFT_X, 28, { width: USABLE_W, align: 'right', lineBreak: false });
  doc.restore();
}

function drawFooter(doc, generatedAt) {
  doc.save();
  const y = PAGE_H - 36;
  doc.font(FONT_REG).fontSize(7).fillColor('#666A72');
  doc.text(
    `Generated by ${PLATFORM_NAME} on ${generatedAt}`,
    LEFT_X, y, { width: USABLE_W, align: 'center', lineBreak: false },
  );
  doc.font(FONT_ITAL).fontSize(7).fillColor('#666A72');
  doc.text(
    'This document is an internal record and is not a regulatory submission. ' +
    'Where regulatory reporting is required, submit through the appropriate regulator\'s channels ' +
    '(OSHA ITA, HSE RIDDOR, SafeWork NSW Notify).',
    LEFT_X, y + 10, { width: USABLE_W, align: 'center', lineBreak: false },
  );
  doc.restore();
}

