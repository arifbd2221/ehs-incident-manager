function sectionCauses(doc, y, ctx) {
  const { fiveWhys, investigation } = ctx;
  y = drawSectionHeader(doc, LEFT_X, y, USABLE_W, 'Root cause analysis', ctx);
  // Categories carried on the investigation row itself (WI-A / activity log).
  let categories = [];
  try { categories = JSON.parse(investigation?.root_cause_categories || '[]'); } catch (_) {}
  if (categories.length > 0) {
    y = drawFieldRow(doc, LEFT_X, y, USABLE_W, 'Categories', categories.join(' · '), ctx);
  }
  if (!Array.isArray(fiveWhys) || fiveWhys.length === 0) {
    y = drawWrapped(doc, LEFT_X, y, USABLE_W, 'No 5-Why chain recorded.', 9, '#666A72', FONT_ITAL);
    return y + 8;
  }
  y = drawSubsectionLabel(doc, LEFT_X, y + 4, '5-Why chain', ctx);
  for (const w of fiveWhys) {
    y = nextPageIfNeeded(doc, y, 30, ctx);
    doc.font(FONT_BOLD).fontSize(8).fillColor('#52525F');
    doc.text(`Why #${w.level}${w.is_root_cause ? ' · ROOT CAUSE' : ''}`, LEFT_X, y, { width: USABLE_W, lineBreak: false });
    y += 11;
    y = drawWrapped(doc, LEFT_X + 8, y, USABLE_W - 8, `Q: ${w.question || ''}`, 9, '#1A1A1A', FONT_REG);
    y = drawWrapped(doc, LEFT_X + 8, y + 2, USABLE_W - 8, `A: ${w.answer || ''}`, 9, '#1A1A1A', FONT_REG);
    y += 4;
  }
  return y + 6;
}

